function countByCategory(products, slug) {
  return products.filter((p) => p.category === slug).length;
}

async function seed() {
  console.log("üå± Starting comprehensive product seeding...");

  if (!uri) {
    console.error("‚ùå MONGODB_URI not found in .env.local");
    process.exit(1);
  }

  const client = new MongoClient(uri);

  try {
    await client.connect();
    console.log("‚úÖ Connected to MongoDB");

    const db = client.db(process.env.MONGODB_DB);
    console.log("üß† Using DB:", process.env.MONGODB_DB);

    // Clear existing data
    console.log("üóëÔ∏è  Clearing existing data...");
    await db.collection("products").deleteMany({});
    await db.collection("categories").deleteMany({});
    console.log("‚úÖ Existing data cleared");

    // Create indexes
    console.log("üìä Creating indexes...");
    await db.collection("products").createIndex({ slug: 1 }, { unique: true });
    await db.collection("products").createIndex({ category: 1 });
    await db.collection("products").createIndex({ featured: 1 });
    await db.collection("products").createIndex({ isFeatured: 1, featuredOrder: 1 });
    await db.collection("categories").createIndex({ slug: 1 }, { unique: true });
    await db.collection("categories").createIndex({ isActive: 1, displayOrder: 1 });
    console.log("‚úÖ Indexes created");

    // Seed categories
    console.log("üìÅ Seeding categories...");
    await db.collection("categories").insertMany(categories);
    console.log(`‚úÖ ${categories.length} categories seeded`);

    // Seed products
    console.log("üì¶ Seeding products...");
    await db.collection("products").insertMany(products);
    console.log(`‚úÖ ${products.length} products seeded`);

    // Update category counts
    console.log("üî¢ Updating category product counts...");
    const electronicsCount = countByCategory(products, "electronics");
    const fashionCount = countByCategory(products, "fashion");
    const sportsCount = countByCategory(products, "sports");
    const booksCount = countByCategory(products, "books");
    const homeCount = countByCategory(products, "home");

    await Promise.all([
      db.collection("categories").updateOne({ slug: "electronics" }, { $set: { productCount: electronicsCount } }),
      db.collection("categories").updateOne({ slug: "fashion" }, { $set: { productCount: fashionCount } }),
      db.collection("categories").updateOne({ slug: "sports" }, { $set: { productCount: sportsCount } }),
      db.collection("categories").updateOne({ slug: "books" }, { $set: { productCount: booksCount } }),
      db.collection("categories").updateOne({ slug: "home" }, { $set: { productCount: homeCount } }),
    ]);

    console.log("‚úÖ Category counts updated");
    console.log("\nüìä Summary:");
    console.log(`   Electronics: ${electronicsCount} products`);
    console.log(`   Fashion: ${fashionCount} products`);
    console.log(`   Sports: ${sportsCount} products`);
    console.log(`   Books: ${booksCount} products`);
    console.log(`   Home & Living: ${homeCount} products`);
    console.log(`   Total: ${products.length} products\n`);

    console.log("üéâ Seeding completed successfully!");
  } catch (error) {
    console.error("‚ùå Seeding failed:", error);
    process.exit(1);
  } finally {
    await client.close();
    console.log("üëã Database connection closed");
  }
}

